package appUsingSpringBasic;

public class Student {
    private String name;
    private String sudyIn;
    private int age;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSudyIn() {
		return sudyIn;
	}
	public void setSudyIn(String sudyIn) {
		this.sudyIn = sudyIn;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
    
}
