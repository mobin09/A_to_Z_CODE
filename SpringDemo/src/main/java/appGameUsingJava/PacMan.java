package appGameUsingJava;

public class PacMan implements Games{
    public void up() {
    	System.out.println("PacMan is Awaken");   
    }
    public void down() {
    	System.out.println("PacMan is sleeping");
    }
    public void left() {
    	System.out.println("PacMan is resting");
    }
    public void right() {
    	System.out.println("PacMan is working");
    }
}
