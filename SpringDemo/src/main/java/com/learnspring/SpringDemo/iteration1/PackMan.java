public class PackMan {
	public void up() {
		System.out.println("Go UP packman");
	}
	
	public void down() {
		System.out.println("Go down packman");
	}
	
	public void left() {
		System.out.println("Go Left Packman");
	}
	
	public void right() {
		System.out.println("Go left packman");
	}
	

}
